console.log(Math.PI); // Retorna o valor de pi (3.141592653589793)
console.log(Math.E); // Retorna o valor da base do logaritmo natural (2.718281828459045)

let numero = 4.6;

console.log(Math.round(numero)); // Arredonda para o número inteiro mais próximo (5)
console.log(Math.floor(numero)); // Arredonda para baixo para o número inteiro mais próximo (4)
console.log(Math.ceil(numero)); // Arredonda para cima para o número inteiro mais próximo (5)

console.log(Math.sqrt(numero)); // Calcula a raiz quadrada (2.1447610589527217)
console.log(Math.random()); // Gera um número aleatório entre 0 (inclusivo) e 1 (exclusivo)


let graus = 45;

let radianos = Math.PI * graus / 180; // Convertendo de graus para radianos
console.log(radianos); // 0.7853981633974483

console.log(Math.sin(radianos)); // Calcula o seno do ângulo (0.7071067811865475)
console.log(Math.cos(radianos)); // Calcula o cosseno do ângulo (0.7071067811865476)
console.log(Math.tan(radianos)); // Calcula a tangente do ângulo (1)


